
package classecalculadora;


public class Calculadora {
     public int multiplicar(int a, int b) {
        return a * b;
    }

    
    public int multiplicar(int a, int b, int c) {
        return a * b * c;
    }

   
    public double multiplicar(double a, double b) {
        return a * b;
    }
    
    
}
